#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	i = 0;
	if (!(s1 || s2))
		return (NULL);
	while (s1[i] != '\0')
		i++;
	j = 0;
	while (s2[j] != '\0' && n > 0)
	{
		s1[i] = s2[j];
		n--;
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
