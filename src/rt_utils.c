#include "rtv1.h"

void	rt_delvec3(t_vec3 **tab)
{
	size_t n;

	if (tab)
	{
		n = 0;
		while (tab[n])
		{
			free(tab[n]);
			++n;
		}
		free(tab);
	}
}
